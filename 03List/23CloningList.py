a = [1, 2, 3, 4, 5]

# Copying the list
b = a.copy()

print(b)

a = [1, 2, 3, 4, 5]

# Copying the list using slicing
b = a[:]

print(b)

a = [1, 2, 3, 4, 5]

# Copying the list using list()
b = list(a)

print(b)

a = [1, 2, 3, 4, 5]

# Copying the list using list comprehension
b = [item for item in a]

print(b)

